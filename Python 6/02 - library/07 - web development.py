# Library Web Development => bertujuan untuk pengembangan aplikasi web

# Django => high-level Python web framework yang mendukung pengembangan secara cepat, bersih, serta pragmatis

# Flask => web framework dalam Python yang ditujukan untuk membangun aplikasi web
    # Flask dirancang dengan tujuan menjadi ringan, fleksibel, dan sederhana
    # Dengan Flask, Anda bisa merancang aplikasi web dari yang sederhana hingga kompleks.

# FastAPI => web framework untuk Python yang tujuannya merancang dan membangun API
    # dengan cepat, efisien, dan aman
    # FastAPI memberikan kinerja yang tinggi, sintaks yang intuitif, serta dukungan otomatisasi dokumentasi 
    # yang kuat. Jadi, ia cocok untuk pengembangan mikroservis, layanan web responsif, dan sebagainya.